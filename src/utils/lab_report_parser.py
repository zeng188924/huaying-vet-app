def _normalize(text: str) -> str:
    if not text:
        return ""
    # 去掉常见空格与换行干扰
    return re.sub(r"\s+", "", str(text))


def parse_lab_report(text: str) -> Optional[Dict]:
    """解析实验室检测报告文本，返回识别出的疾病和症状摘要。

    返回示例：
        {
            "disease_category": "呼吸道疾病",
            "disease_name": "慢性呼吸道病（支原体）",
            "symptom_summary": "检测到支原体，伴随咳嗽、甩鼻...",
            "confidence": "高",
            "raw_text": "...",
        }
    """
    if not text or not text.strip():
        return None

    norm = _normalize(text)
    best_category = None
    best_disease = None
    best_score = 0

    for category, diseases in DISEASE_KEYWORDS.items():
        for disease, keywords in diseases.items():
            score = sum(1 for kw in keywords if kw in norm)
            if score > best_score:
                best_score = score
                best_category = category
                best_disease = disease

    if best_score == 0:
        return None

    found_symptoms = [kw for kw in SYMPTOM_KEYWORDS if kw in norm]
    symptom_summary = "、".join(found_symptoms[:8]) if found_symptoms else ""

    confidence = "高" if best_score >= 3 else ("中" if best_score == 2 else "低")

    return {
        "disease_category": best_category,
        "disease_name": best_disease,
        "symptom_summary": symptom_summary,
        "confidence": confidence,
        "raw_text": text.strip(),
    }
